# Effective Number of Bets (matches C# EffectiveNumberOfBets — Jacobi)
# ═══════════════════════════════════════════════════════════════════════════

def effective_number_of_bets(corr_matrix):
    """ENB via eigenvalue entropy. Uses numpy eigh (not Jacobi).
    C# uses Jacobi, so expect PrecisionStatistical for eigenvector-derived values."""
    eigenvalues = np.linalg.eigvalsh(corr_matrix)
    eigenvalues = np.maximum(eigenvalues, 0.0)  # Clamp negatives
    eigenvalues = np.sort(eigenvalues)[::-1]     # Sort descending

    total = eigenvalues.sum()
    if total <= 0:
        return 1.0, eigenvalues

    probs = eigenvalues / total
    entropy = 0.0
    for p in probs:
        if p > 0:
            entropy -= p * math.log(p)

    enb = math.exp(entropy)
    return enb, eigenvalues


