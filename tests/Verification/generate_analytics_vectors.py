# Correlation Analyzer (matches C# CorrelationAnalyzer)
# ═══════════════════════════════════════════════════════════════════════════

def correlation_analysis(returns, weights):
    """Compute correlation matrix and diversification ratio. Matches C#."""
    n = returns.shape[0]
    t_obs = returns.shape[1]

    # Sample covariance (N-1 divisor)
    means = returns.mean(axis=1)
    cov = np.zeros((n, n))
    for i in range(n):
        for j in range(i, n):
            c = sum((returns[i, k] - means[i]) * (returns[j, k] - means[j])
                    for k in range(t_obs)) / (t_obs - 1)
            cov[i, j] = c
            cov[j, i] = c

    # Correlation matrix
    stds = np.sqrt(np.diag(cov))
    corr = np.zeros((n, n))
    for i in range(n):
        for j in range(n):
            if stds[i] == 0 or stds[j] == 0:
                corr[i, j] = 1.0 if i == j else 0.0
            else:
                corr[i, j] = cov[i, j] / (stds[i] * stds[j])

    # Diversification ratio
    weighted_avg_vol = sum(weights[i] * stds[i] for i in range(n))
    port_var = sum(weights[i] * weights[j] * cov[i, j]
                   for i in range(n) for j in range(n))
    port_vol = math.sqrt(max(0.0, port_var))
    dr = weighted_avg_vol / port_vol if port_vol > 0 else 1.0

    return {
        "correlation_matrix": corr,
        "diversification_ratio": dr,
        "covariance_matrix": cov,
        "std_devs": stds,
    }


