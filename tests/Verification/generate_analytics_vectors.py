# Factor Regression (matches C# FactorRegressor — Gaussian elimination)
# ═══════════════════════════════════════════════════════════════════════════

def factor_regression(portfolio_returns, factor_names, factor_returns):
    """OLS regression via normal equations with Gaussian elimination + partial pivoting.
    Matches C# FactorRegressor exactly."""
    t_obs = len(portfolio_returns)
    k = len(factor_names)

    # Build X matrix: T × (K+1) — intercept + factors
    x = np.ones((t_obs, k + 1))
    for j, name in enumerate(factor_names):
        x[:, j + 1] = factor_returns[name]

    y = np.array(portfolio_returns)

    # X'X and X'y
    xtx = x.T @ x
    xty = x.T @ y

    # Gaussian elimination with partial pivoting
    n_cols = k + 1
    aug = np.zeros((n_cols, n_cols + 1))
    aug[:, :n_cols] = xtx
    aug[:, n_cols] = xty

    for col in range(n_cols):
        # Partial pivoting: find max in column
        max_row = col
        max_val = abs(aug[col, col])
        for row in range(col + 1, n_cols):
            if abs(aug[row, col]) > max_val:
                max_val = abs(aug[row, col])
                max_row = row
        if max_row != col:
            aug[[col, max_row]] = aug[[max_row, col]]

        pivot = aug[col, col]
        if abs(pivot) < 1e-14:
            raise ValueError("Singular matrix in factor regression")

        # Eliminate below
        for row in range(col + 1, n_cols):
            factor = aug[row, col] / pivot
            aug[row, col:] -= factor * aug[col, col:]

    # Back substitution
    beta = np.zeros(n_cols)
    for i in range(n_cols - 1, -1, -1):
        beta[i] = (aug[i, n_cols] - sum(aug[i, j] * beta[j]
                                          for j in range(i + 1, n_cols))) / aug[i, i]

    alpha = beta[0]
    factor_loadings = {name: beta[j + 1] for j, name in enumerate(factor_names)}

    # R² and residual std error
    y_pred = x @ beta
    residuals = y - y_pred
    ss_res = np.sum(residuals ** 2)
    y_mean = np.mean(y)
    ss_tot = np.sum((y - y_mean) ** 2)
    r_squared = max(0.0, min(1.0, 1.0 - ss_res / ss_tot)) if ss_tot > 0 else 0.0

    dof = t_obs - k - 1
    residual_std_error = math.sqrt(ss_res / dof) if dof > 0 else 0.0

    return {
        "alpha": alpha,
        "factor_loadings": factor_loadings,
        "r_squared": r_squared,
        "residual_std_error": residual_std_error,
    }


