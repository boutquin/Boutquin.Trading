# Generator: FactorRegressor
# ═══════════════════════════════════════════════════════════════════════════

def gen_factor_regression():
    """Phase 4E: Factor Regression — OLS with Gaussian elimination."""
    print("Phase 4E: Factor Regression")

    # Generate synthetic factor data
    t_obs = 120
    factor_names = ["Mkt-RF", "SMB", "HML"]
    factor_data = {
        "Mkt-RF": RNG.normal(0.005, 0.04, t_obs),
        "SMB": RNG.normal(0.002, 0.03, t_obs),
        "HML": RNG.normal(0.001, 0.025, t_obs),
    }

    # Generate portfolio returns with known loadings
    true_alpha = 0.001
    true_betas = {"Mkt-RF": 1.1, "SMB": 0.3, "HML": -0.2}
    noise = RNG.normal(0, 0.01, t_obs)
    portfolio_returns = (true_alpha +
                         true_betas["Mkt-RF"] * factor_data["Mkt-RF"] +
                         true_betas["SMB"] * factor_data["SMB"] +
                         true_betas["HML"] * factor_data["HML"] +
                         noise)

    result = factor_regression(portfolio_returns.tolist(),
                                factor_names,
                                {k: v.tolist() for k, v in factor_data.items()})

    # --- Layer 1: Library cross-check vs statsmodels ---
    try:
        import statsmodels.api as sm
        x_sm = np.column_stack([factor_data[n] for n in factor_names])
        x_sm = sm.add_constant(x_sm)
        model = sm.OLS(portfolio_returns, x_sm).fit()
        quality_check("FactorReg: alpha vs statsmodels", result["alpha"], model.params[0])
        quality_check("FactorReg: R² vs statsmodels", result["r_squared"], model.rsquared)
        for j, name in enumerate(factor_names):
            quality_check(f"FactorReg: beta_{name} vs statsmodels",
                          result["factor_loadings"][name], model.params[j + 1])
    except ImportError:
        print("  statsmodels not available — skipping Layer 1 cross-check")

    # --- Layer 2: Analytical ---
    # R² should be high since we generated data with known structure
    hard_check("FactorReg: R² > 0.8 (structured data)", result["r_squared"] > 0.8)

    # Perfect linear data: R² = 1
    perfect_returns = (0.002 +
                       1.5 * factor_data["Mkt-RF"] +
                       0.5 * factor_data["SMB"] -
                       0.3 * factor_data["HML"])
    result_perfect = factor_regression(perfect_returns.tolist(),
                                        factor_names,
                                        {k: v.tolist() for k, v in factor_data.items()})
    hard_check("FactorReg: perfect data → R²≈1", abs(result_perfect["r_squared"] - 1.0) < 1e-8)
    hard_check("FactorReg: perfect data → alpha≈0.002", abs(result_perfect["alpha"] - 0.002) < 1e-8)
    hard_check("FactorReg: perfect data → beta_Mkt≈1.5",
               abs(result_perfect["factor_loadings"]["Mkt-RF"] - 1.5) < 1e-8)

    # --- Layer 3: Properties ---
    hard_check("FactorReg: R² ∈ [0,1]", 0.0 <= result["r_squared"] <= 1.0)
    hard_check("FactorReg: residual std > 0", result["residual_std_error"] > 0)

    # Single factor
    result_1f = factor_regression(portfolio_returns.tolist(),
                                   ["Mkt-RF"],
                                   {"Mkt-RF": factor_data["Mkt-RF"].tolist()})
    hard_check("FactorReg: single factor R² ∈ [0,1]", 0.0 <= result_1f["r_squared"] <= 1.0)

    # 5-factor
    factor_data_5 = dict(factor_data)
    factor_data_5["RMW"] = RNG.normal(0.001, 0.02, t_obs)
    factor_data_5["CMA"] = RNG.normal(0.001, 0.02, t_obs)
    factor_names_5 = ["Mkt-RF", "SMB", "HML", "RMW", "CMA"]
    result_5f = factor_regression(portfolio_returns.tolist(),
                                   factor_names_5,
                                   {k: v.tolist() for k, v in factor_data_5.items()})
    hard_check("FactorReg: 5-factor R² ∈ [0,1]", 0.0 <= result_5f["r_squared"] <= 1.0)

    save("analytics_factor_regression", {
        "cases": {
            "three_factor": {
                "portfolio_returns": portfolio_returns.tolist(),
                "factor_names": factor_names,
                "factor_returns": {k: v.tolist() for k, v in factor_data.items()},
                **result,
            },
            "perfect_linear": {
                "portfolio_returns": perfect_returns.tolist(),
                "factor_names": factor_names,
                "factor_returns": {k: v.tolist() for k, v in factor_data.items()},
                **result_perfect,
            },
            "single_factor": {
                "portfolio_returns": portfolio_returns.tolist(),
                "factor_names": ["Mkt-RF"],
                "factor_returns": {"Mkt-RF": factor_data["Mkt-RF"].tolist()},
                **result_1f,
            },
            "five_factor": {
                "portfolio_returns": portfolio_returns.tolist(),
                "factor_names": factor_names_5,
                "factor_returns": {k: v.tolist() for k, v in factor_data_5.items()},
                **result_5f,
            },
        },
        "notes": "OLS factor regression via Gaussian elimination with partial pivoting. "
                 "Matches C# FactorRegressor.",
    })


