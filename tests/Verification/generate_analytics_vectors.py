# Brinson-Fachler Attribution (matches C# BrinsonFachlerAttributor)
# ═══════════════════════════════════════════════════════════════════════════

def brinson_fachler(assets, port_weights, bench_weights, port_returns, bench_returns):
    """Single-period Brinson-Fachler attribution. Matches C#."""
    bench_total = sum(bench_weights[a] * bench_returns[a] for a in assets)

    alloc_effects = {}
    sel_effects = {}
    inter_effects = {}

    for a in assets:
        wp = port_weights[a]
        wb = bench_weights[a]
        rp = port_returns[a]
        rb = bench_returns[a]
        alloc_effects[a] = (wp - wb) * (rb - bench_total)
        sel_effects[a] = wb * (rp - rb)
        inter_effects[a] = (wp - wb) * (rp - rb)

    total_alloc = sum(alloc_effects.values())
    total_sel = sum(sel_effects.values())
    total_inter = sum(inter_effects.values())
    total_active = total_alloc + total_sel + total_inter

    return {
        "allocation_effect": total_alloc,
        "selection_effect": total_sel,
        "interaction_effect": total_inter,
        "total_active_return": total_active,
        "asset_allocation_effects": alloc_effects,
        "asset_selection_effects": sel_effects,
        "asset_interaction_effects": inter_effects,
    }


