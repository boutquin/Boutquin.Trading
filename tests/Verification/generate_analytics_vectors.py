# Drawdown Analyzer (matches C# DrawdownAnalyzer)
# ═══════════════════════════════════════════════════════════════════════════

def analyze_drawdown_periods(dates, equity_values):
    """Identify drawdown periods. Matches C# DrawdownAnalyzer.AnalyzeDrawdownPeriods."""
    periods = []
    n = len(dates)

    peak_val = equity_values[0]
    peak_date = dates[0]
    peak_idx = 0
    trough_val = equity_values[0]
    trough_date = dates[0]
    trough_idx = 0
    in_drawdown = False

    for i in range(1, n):
        val = equity_values[i]

        if val >= peak_val:
            if in_drawdown:
                # Recovery — record the drawdown period
                depth = (trough_val - peak_val) / peak_val
                # C# uses i - peakIndex (peak to recovery), not peak to trough
                duration_days = i - peak_idx
                recovery_days = i - trough_idx
                periods.append({
                    "start_date": peak_date.isoformat(),
                    "trough_date": trough_date.isoformat(),
                    "recovery_date": dates[i].isoformat(),
                    "depth": depth,
                    "duration_days": duration_days,
                    "recovery_days": recovery_days,
                })
                in_drawdown = False

            peak_val = val
            peak_date = dates[i]
            peak_idx = i
            trough_val = val
            trough_date = dates[i]
            trough_idx = i
        else:
            in_drawdown = True
            if val < trough_val:
                trough_val = val
                trough_date = dates[i]
                trough_idx = i

    # Handle ongoing drawdown at end
    if in_drawdown:
        depth = (trough_val - peak_val) / peak_val
        # C# uses entries.Count - 1 - peakIndex (peak to end of series)
        duration_days = n - 1 - peak_idx
        periods.append({
            "start_date": peak_date.isoformat(),
            "trough_date": trough_date.isoformat(),
            "recovery_date": None,
            "depth": depth,
            "duration_days": duration_days,
            "recovery_days": None,
        })

    return periods


