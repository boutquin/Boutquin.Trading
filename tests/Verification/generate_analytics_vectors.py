def gen_brinson_fachler():
    """Phase 4D: BrinsonFachler Attribution."""
    print("Phase 4D: BrinsonFachler Attribution")

    # Standard 4-sector case
    assets = ["Equities", "Bonds", "Commodities", "RealEstate"]
    pw = {"Equities": 0.50, "Bonds": 0.25, "Commodities": 0.15, "RealEstate": 0.10}
    bw = {"Equities": 0.40, "Bonds": 0.35, "Commodities": 0.15, "RealEstate": 0.10}
    pr = {"Equities": 0.10, "Bonds": 0.03, "Commodities": -0.02, "RealEstate": 0.06}
    br = {"Equities": 0.08, "Bonds": 0.04, "Commodities": -0.01, "RealEstate": 0.05}

    result_4sec = brinson_fachler(assets, pw, bw, pr, br)

    # --- Layer 2: Analytical ---
    # Effects must sum to total active return
    port_total = sum(pw[a] * pr[a] for a in assets)
    bench_total = sum(bw[a] * br[a] for a in assets)
    active_return = port_total - bench_total
    hard_check("BF: effects sum to active return",
               abs(result_4sec["total_active_return"] - active_return) < 1e-14)
    hard_check("BF: alloc+sel+inter = total",
               abs(result_4sec["allocation_effect"] + result_4sec["selection_effect"] +
                   result_4sec["interaction_effect"] - result_4sec["total_active_return"]) < 1e-14)

    # Identical weights: allocation = 0, interaction = 0
    same_w = {"Equities": 0.40, "Bonds": 0.35, "Commodities": 0.15, "RealEstate": 0.10}
    result_same_w = brinson_fachler(assets, same_w, same_w, pr, br)
    hard_check("BF: identical weights → allocation=0", abs(result_same_w["allocation_effect"]) < 1e-14)
    hard_check("BF: identical weights → interaction=0", abs(result_same_w["interaction_effect"]) < 1e-14)

    # Identical returns: selection = 0, interaction = 0
    same_r = {"Equities": 0.08, "Bonds": 0.04, "Commodities": -0.01, "RealEstate": 0.05}
    result_same_r = brinson_fachler(assets, pw, bw, same_r, same_r)
    hard_check("BF: identical returns → selection=0", abs(result_same_r["selection_effect"]) < 1e-14)
    hard_check("BF: identical returns → interaction=0", abs(result_same_r["interaction_effect"]) < 1e-14)

    # --- Layer 3: Properties ---
    # Per-asset effects sum to totals
    hard_check("BF: per-asset alloc sums",
               abs(sum(result_4sec["asset_allocation_effects"].values()) -
                   result_4sec["allocation_effect"]) < 1e-14)
    hard_check("BF: per-asset sel sums",
               abs(sum(result_4sec["asset_selection_effects"].values()) -
                   result_4sec["selection_effect"]) < 1e-14)
    hard_check("BF: per-asset inter sums",
               abs(sum(result_4sec["asset_interaction_effects"].values()) -
                   result_4sec["interaction_effect"]) < 1e-14)

    # Edge: 2-sector
    assets_2 = ["Equities", "Bonds"]
    pw_2 = {"Equities": 0.70, "Bonds": 0.30}
    bw_2 = {"Equities": 0.60, "Bonds": 0.40}
    pr_2 = {"Equities": 0.12, "Bonds": 0.02}
    br_2 = {"Equities": 0.10, "Bonds": 0.03}
    result_2sec = brinson_fachler(assets_2, pw_2, bw_2, pr_2, br_2)
    hard_check("BF 2-sector: effects sum to active",
               abs(result_2sec["total_active_return"] -
                   (sum(pw_2[a] * pr_2[a] for a in assets_2) -
                    sum(bw_2[a] * br_2[a] for a in assets_2))) < 1e-14)

    save("analytics_brinson_fachler", {
        "cases": {
            "four_sector": {
                "assets": assets,
                "portfolio_weights": pw,
                "benchmark_weights": bw,
                "portfolio_returns": pr,
                "benchmark_returns": br,
                **result_4sec,
            },
            "identical_weights": {
                "assets": assets,
                "portfolio_weights": same_w,
                "benchmark_weights": same_w,
                "portfolio_returns": pr,
                "benchmark_returns": br,
                **result_same_w,
            },
            "identical_returns": {
                "assets": assets,
                "portfolio_weights": pw,
                "benchmark_weights": bw,
                "portfolio_returns": same_r,
                "benchmark_returns": same_r,
                **result_same_r,
            },
            "two_sector": {
                "assets": assets_2,
                "portfolio_weights": pw_2,
                "benchmark_weights": bw_2,
                "portfolio_returns": pr_2,
                "benchmark_returns": br_2,
                **result_2sec,
            },
        },
        "notes": "Brinson-Fachler single-period attribution. "
                 "Alloc=(Wp-Wb)(Rb_i-Rb_total), Sel=Wb(Rp_i-Rb_i), Inter=(Wp-Wb)(Rp_i-Rb_i).",
    })


