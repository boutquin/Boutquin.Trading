def gen_drawdown():
    """Phase 4H: DrawdownAnalyzer — period detection."""
    print("Phase 4H: DrawdownAnalyzer")

    # Generate equity curve with known drawdown pattern
    base_date = date(2023, 1, 2)
    daily_returns = RNG.normal(0.0005, 0.015, 252)
    # Inject a clear drawdown: days 50-80 have negative returns
    daily_returns[50:65] = -0.02
    daily_returns[65:80] = 0.015  # Recovery

    equity = [10000.0]
    dates_list = [base_date]
    for i, r in enumerate(daily_returns):
        equity.append(equity[-1] * (1 + r))
        dates_list.append(base_date + timedelta(days=i + 1))

    periods = analyze_drawdown_periods(dates_list, equity)

    # --- Layer 2: Analytical ---
    # At least one drawdown should exist (we injected one)
    hard_check("Drawdown: at least 1 period detected", len(periods) >= 1)

    # Monotonically increasing → no drawdowns
    mono_equity = [10000.0 + i * 10 for i in range(100)]
    mono_dates = [base_date + timedelta(days=i) for i in range(100)]
    mono_periods = analyze_drawdown_periods(mono_dates, mono_equity)
    hard_check("Drawdown: monotonic → 0 periods", len(mono_periods) == 0)

    # Single dip and recovery
    simple_equity = [100.0, 95.0, 90.0, 95.0, 100.0, 105.0]
    simple_dates = [base_date + timedelta(days=i) for i in range(6)]
    simple_periods = analyze_drawdown_periods(simple_dates, simple_equity)
    hard_check("Drawdown: simple dip → 1 period", len(simple_periods) == 1)
    if len(simple_periods) == 1:
        hard_check("Drawdown: simple depth ≈ -10%", abs(simple_periods[0]["depth"] - (-0.10)) < 1e-10)
        # duration = recovery_idx(4) - peak_idx(0) = 4 (peak to recovery, matching C#)
        hard_check("Drawdown: simple duration = 4", simple_periods[0]["duration_days"] == 4)
        hard_check("Drawdown: simple recovery = 2", simple_periods[0]["recovery_days"] == 2)

    # --- Layer 3: Properties ---
    for p in periods:
        hard_check(f"Drawdown: depth ∈ [-1,0] ({p['depth']:.4f})", -1.0 <= p["depth"] <= 0.0)
        hard_check(f"Drawdown: duration > 0", p["duration_days"] > 0)
        if p["recovery_days"] is not None:
            hard_check(f"Drawdown: recovery ≥ 0", p["recovery_days"] >= 0)

    # Ongoing drawdown (no recovery at end)
    declining_equity = [100.0, 95.0, 90.0, 85.0, 80.0]
    declining_dates = [base_date + timedelta(days=i) for i in range(5)]
    declining_periods = analyze_drawdown_periods(declining_dates, declining_equity)
    hard_check("Drawdown: declining → 1 ongoing period", len(declining_periods) == 1)
    if len(declining_periods) == 1:
        hard_check("Drawdown: ongoing has no recovery date",
                   declining_periods[0]["recovery_date"] is None)
        hard_check("Drawdown: ongoing has no recovery days",
                   declining_periods[0]["recovery_days"] is None)
        hard_check("Drawdown: ongoing depth = -20%",
                   abs(declining_periods[0]["depth"] - (-0.20)) < 1e-10)

    save("analytics_drawdown", {
        "cases": {
            "full_year": {
                "dates": [d.isoformat() for d in dates_list],
                "equity": equity,
                "periods": periods,
            },
            "monotonic": {
                "dates": [d.isoformat() for d in mono_dates],
                "equity": mono_equity,
                "periods": mono_periods,
            },
            "simple_dip": {
                "dates": [d.isoformat() for d in simple_dates],
                "equity": simple_equity,
                "periods": simple_periods,
            },
            "ongoing_decline": {
                "dates": [d.isoformat() for d in declining_dates],
                "equity": declining_equity,
                "periods": declining_periods,
            },
        },
        "notes": "DrawdownAnalyzer: tracks peak→trough→recovery. Duration in trading days (index-based).",
    })


